    private void jButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanActionPerformed
       try {

            String name = jTextFieldNama.getText();
            String nidn = jTextFieldNIDN.getText();
            String cardID = jTextFieldCardID.getText();
            String expertise = jTextFieldExpertise.getText();

            if (name.isEmpty() || name.equals("Nama") || nidn.isEmpty() || nidn.equals("NIDN") || cardID.isEmpty() || cardID.equals("CardID") || expertise.isEmpty() || expertise.equals("Expertise")) {
                javax.swing.JOptionPane.showMessageDialog(this,
                        "Data kolom dosen harus terisi",
                        "Peringatan",
                        javax.swing.JOptionPane.WARNING_MESSAGE);
                return;
            }

            Lecturer lecturer = new Lecturer(cardID, name, nidn, expertise);

            int hasil = 0;
            String messageSucces = "";
            if(this.nidnLama.equals("")){
                hasil = controller.create(lecturer);
                messageSucces = "Data dosen berhasil disimpan!"; 
            }else{
                hasil = controller.update(lecturer, this.nidnLama);
                messageSucces = "Data dosen berhasil diperbarui!"; 
            }
               
            if (hasil == 1) {
              
                javax.swing.JOptionPane.showMessageDialog(this, messageSucces);

                loadLecturerData();
                clearForm();
            } 

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Terjadi kesalahan sistem: " + e.getMessage(),
                    "Error Fatal",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButtonSimpanActionPerformed

    private void jButtonPrevActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPrevActionPerformed
        // TODO add your handling code here:
        if (listDosen == null || listDosen.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Belum ada data dosen pada tabel");
            return;
        }
        if (currentIndex > 0) {
            currentIndex--;
            tampilkanData(currentIndex);
            jTableLecturer.setRowSelectionInterval(currentIndex, currentIndex);
            jTableLecturer.changeSelection(currentIndex, 0, false, false);
            jTableLecturer.scrollRectToVisible(jTableLecturer.getCellRect(currentIndex, 0, true));

            jTableLecturer.requestFocusInWindow();
        }
    }//GEN-LAST:event_jButtonPrevActionPerformed

    private void jButtonNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonNextActionPerformed
        // TODO add your handling code here:
        if (listDosen == null || listDosen.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Belum ada data dosen pada tabel");
            return;
        }
        if (currentIndex < listDosen.size() - 1) {
            currentIndex++;
            tampilkanData(currentIndex);
            jTableLecturer.setRowSelectionInterval(currentIndex, currentIndex);
            jTableLecturer.scrollRectToVisible(jTableLecturer.getCellRect(currentIndex, 0, true));

        }
    }//GEN-LAST:event_jButtonNextActionPerformed

    private void jToggleButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButtonDeleteActionPerformed
        // TODO add your handling code here:

        if (this.selectedNIDN == null || this.selectedNIDN.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih data tabel dosen terlebih dahulu", "Peringatan", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        int confirm = javax.swing.JOptionPane.showConfirmDialog(this,
                "Apakah anda yakin untuk menghapus data dosen ini ? ",
                "Delete Confirmation",
                javax.swing.JOptionPane.YES_NO_OPTION
        );

        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            try {
                controller.delete(selectedNIDN);
                javax.swing.JOptionPane.showMessageDialog(this, "Lecturer deleted successfully!");

                this.selectedNIDN = null;
                this.clearForm();
                loadLecturerData();

            } catch (Exception ex) {
                System.err.println("Error saat menghapus data dosen: " + ex.getMessage());
                javax.swing.JOptionPane.showMessageDialog(this,
                        "Deleted unsuccessfully! Terjadi kesalahan pada sistem.",
                        "Error",
                        javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jToggleButtonDeleteActionPerformed

