    @SuppressWarnings("unchecked")
    private void loadLecturerData() throws SQLException {
        try {
//            List<Lecturer> lecturers = controller.getLecturer();
            listDosen = controller.getLecturer();
            String[][] data = new String[listDosen.size()][4];

            for (int i = 0; i < listDosen.size(); i++) {
                data[i][0] = listDosen.get(i).getName();
                data[i][1] = listDosen.get(i).getIdCard();
                data[i][2] = listDosen.get(i).getNidn();
                data[i][3] = listDosen.get(i).getExpertise();
            }

            String[] columnName = {"Name", "ID", "NIDN", "Expertise"};
            jTableLecturer.setModel(new javax.swing.table.DefaultTableModel(data, columnName));

        } catch (Exception e) {
            
            System.err.println("Gagal memuat data dari database: " + e.getMessage());
            e.printStackTrace();
        }
    }

     private void tampilkanData(int index) {
       try {
            if (index >= 0 && index < jTableLecturer.getRowCount()) {
              
                String name = jTableLecturer.getValueAt(index, 0).toString();
                String nik = jTableLecturer.getValueAt(index, 1).toString();
                String nidn = jTableLecturer.getValueAt(index, 2).toString();
                String expertise = jTableLecturer.getValueAt(index, 3).toString();


                jTextFieldNama.setText(name);
                jTextFieldCardID.setText(nik);     
                jTextFieldNIDN.setText(nidn);
                jTextFieldExpertise.setText(expertise);
                
             
                jTextFieldNIDN.setEditable(false);
                

                this.selectedNIDN = nidn;
                this.nidnLama = nidn;
            }
        } catch (Exception e) {
            System.err.println("Gagal menampilkan data ke form: " + e.getMessage());
            e.printStackTrace();
        }
    }
    private void refreshLecturerTable(List<Lecturer> dataList) {
       
        this.listDosen = dataList; 
        

        DefaultTableModel model = (DefaultTableModel) jTableLecturer.getModel();
        model.setRowCount(0);

 
        for (Lecturer r : dataList) {
            Object[] newLine = new Object[]{
                r.getName(),
                r.getIdCard(),
                r.getNidn(),
                r.getExpertise()
            };
            model.addRow(newLine);
        }

       
        if (jTableLecturer.getRowCount() > 0) {
            jTableLecturer.setRowSelectionInterval(0, 0);
            currentIndex = 0; 
            tampilkanData(currentIndex);
        }
    }
