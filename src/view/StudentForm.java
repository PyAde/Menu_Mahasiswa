    private void jButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanActionPerformed
        // TODO add your handling code here:
        String name = jTextFieldNama.getText();
        String cardID = jTextFieldNIK.getText();
        String NIM = jTextFieldNIM.getText();
        String studyProgram = jComboBoxProdi.getSelectedItem().toString();

        if (name.isEmpty() || cardID.isEmpty() || NIM.isEmpty() || studyProgram.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields", "Input Error", JOptionPane.ERROR_MESSAGE);
        } else {
            Student student = new Student(cardID, name, NIM, studyProgram);
            try {
                int res = 0;
                String messageSuccess = "";
                String messageError = "";

                if (this.selectedNIM == null || this.selectedNIM.equals("")) {
                    res = controller.create(student);
                    messageSuccess = "Student created successfully!";
                    messageError = "Error occurred while inserting data.";
                } else {
                    res = controller.update(student, this.selectedNIM);
                    messageSuccess = "Student updated successfully!";
                    messageError = "Error occurred while updating data.";
                }
                if (res == 1) {
                    JOptionPane.showMessageDialog(this, messageSuccess, "Success", JOptionPane.INFORMATION_MESSAGE);
                    this.selectedNIM = null; 
                    this.clearForm();
                    loadStudentData();
                } else {
                    JOptionPane.showMessageDialog(this, messageError, "Error", JOptionPane.ERROR_MESSAGE);
                }
                
            } catch (Exception ex) { 
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + ex.getMessage(), "Error Fatal", JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButtonSimpanActionPerformed

    private void jTableStudentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableStudentMouseClicked
        // TODO add your handling code here:
        int selecttedRow = jTableStudent.getSelectedRow();
        if (selecttedRow != -1) {
            currentIndex = selecttedRow;
            jTextFieldNIM.setEditable(false);
            selectedNIM = jTableStudent.getValueAt(selecttedRow, 2).toString();
            jTextFieldNIK.setText(jTableStudent.getValueAt(selecttedRow, 0).toString());
            jTextFieldNama.setText(jTableStudent.getValueAt(selecttedRow, 1).toString());
            jTextFieldNIM.setText(jTableStudent.getValueAt(selecttedRow, 2).toString());
            jComboBoxProdi.setSelectedItem(jTableStudent.getValueAt(selecttedRow, 3).toString());
        }
    }//GEN-LAST:event_jTableStudentMouseClicked

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        int confirm = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to delete this student?",
                "Delete Confirmation",
                JOptionPane.YES_NO_OPTION
        );

        if (confirm == JOptionPane.YES_OPTION) {
            try {
                controller.delete(selectedNIM);
                JOptionPane.showMessageDialog(this, "Student deleted successfully!");
                this.selectedNIM = null;
                this.clearForm();
                loadStudentData();
            } catch (SQLException ex) {
                Logger.getLogger(StudentForm.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "deleted unsuccessfully!");
            }
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

