    private void jButtonNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonNextActionPerformed
        // TODO add your handling code here:

        if (listMahasiswa == null || listMahasiswa.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Belum terdapat data mahasiswa");
            return;
        }
        if (currentIndex < listMahasiswa.size() - 1) {
            currentIndex++;
            tampilkanData(currentIndex);
            jTableStudent.setRowSelectionInterval(currentIndex, currentIndex);
            jTableStudent.scrollRectToVisible(jTableStudent.getCellRect(currentIndex, 0, true));
        }
    }//GEN-LAST:event_jButtonNextActionPerformed

