    private void loadStudentData() throws SQLException {
        List<Student> students = controller.getStudent();
        String[][] data = new String[students.size()][5];

        for (int i = 0; i < students.size(); i++) {
            data[i][0] = String.valueOf(students.get(i).getNIK());
            data[i][1] = students.get(i).getName();
            data[i][2] = students.get(i).getNim();
            data[i][3] = students.get(i).getStudyProgram();
        }
        String[] columnName = {"NIK", "Name", "NIM", "Studi Program"};
        jTableStudent.setModel(new javax.swing.table.DefaultTableModel(data, columnName));
    }

    private void tampilkanData(int index) {
        try {
            if (index >= 0 && index < jTableStudent.getRowCount()) {
                String nik = jTableStudent.getValueAt(index, 0).toString();
                String nama = jTableStudent.getValueAt(index, 1).toString();
                String nim = jTableStudent.getValueAt(index, 2).toString();
                String prodi = jTableStudent.getValueAt(index, 3).toString();

                jTextFieldNIK.setText(nik);
                jTextFieldNama.setText(nama);
                jTextFieldNIM.setText(nim);
                jComboBoxProdi.setSelectedItem(prodi);
            } 

        } catch (Exception e) {
           
           
            e.printStackTrace();
        }
    }
    private void refreshStudentTable(List<Student> dataList) {

        this.listMahasiswa = new ArrayList<>(dataList);


        DefaultTableModel model = (DefaultTableModel) jTableStudent.getModel();
        model.setRowCount(0);

     
        for (Student s : dataList) {
            Object[] newLine = new Object[]{
                s.getNIK(),
                s.getName(),
                s.getNim(),
                s.getStudyProgram()
            };
            model.addRow(newLine);
        }

       
        if (jTableStudent.getRowCount() > 0) {
            jTableStudent.setRowSelectionInterval(0, 0);
            currentIndex = 0;
            tampilkanData(currentIndex);
        }
    }
