    private void validasiFormInputNilai() {
        try {
            boolean isMhsSelected = jComboBoxMhs.getSelectedItem() != null;
            boolean isCourseSelected = jComboBoxCourse.getSelectedItem() != null;
            boolean isDosenSelected = jComboBoxDosen.getSelectedItem() != null;

            boolean isSikapFilled = !jTextFieldNilaiSikap.getText().trim().isEmpty();
            boolean isUtsFilled = !jTextFieldNilaiUTS.getText().trim().isEmpty();
            boolean isUasFilled = !jTextFieldNilaiUAS.getText().trim().isEmpty();

            if (isMhsSelected && isCourseSelected && isDosenSelected
                    && isSikapFilled && isUtsFilled && isUasFilled) {

                jButtonAdd.setEnabled(true);

            } else {
                jButtonAdd.setEnabled(false);

            }

        } catch (Exception e) {
            jButtonAdd.setEnabled(false);
        }
    }
