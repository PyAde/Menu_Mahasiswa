    private void loadKrsData() {
        refreshKrsTable(krsController.getAllKRS());
    }

    private void refreshKrsTable(List<KRS> dataKrs) {
        listKrs = new ArrayList<>(dataKrs);
        DefaultTableModel model = (DefaultTableModel) jTableNilai.getModel();
        model.setRowCount(0);
        for (KRS krs : listKrs) {
            String namaMhs = "-";
            for (Student s : listMahasiswa) {
                if (s.getNim().equals(krs.getStudent().getNim())) {
                    namaMhs = s.getName();
                    break;
                }
            }
            String namaMk = "-";
            String sksMk = "0";
            for (Course c : listMK) {
                if (c.getCode().equals(krs.getCourse().getCode())) {
                    namaMk = c.getCourseName();
                    sksMk = String.valueOf(c.getSKS());
                    break;
                }
            }
            String namaDosen = "-";
            if (krs.getLecture() != null) {
                for (Lecturer l : listLecture) {
                    if (l.getNidn().equals(krs.getLecture().getNidn())) {
                        namaDosen = l.getName();
                        break;
                    }
                }
            }
            Object[] rowData = {
                krs.getStudent().getNim(),
                namaMhs,
                namaMk,
                sksMk,
                krs.getGrade(),
                namaDosen
            };
            model.addRow(rowData);
        }

        if (!listKrs.isEmpty()) {
            currentIndex = 0;
            jTableNilai.setRowSelectionInterval(0, 0);
            tampilkanData(currentIndex);
        } else {
            currentIndex = 0;
            jLabelNIM.setText("");
            jLabelKodeSKS.setText("Kode SKS");
            jLabelHuruf.setText("Nilai Huruf");
        }
    }

    private void tampilkanData(int index) {
        try {
            if (index < 0 || index >= listKrs.size()) {
                return;
            }

            KRS selectedKrs = listKrs.get(index);

            Student mahasiswa = null;
            if (selectedKrs.getStudent() != null) {
                for (Student s : listMahasiswa) {
                    if (s.getNim().equals(selectedKrs.getStudent().getNim())) {
                        mahasiswa = s;
                        jComboBoxMhs.setSelectedItem(s);
                        break;
                    }
                }
            }

            Course course = null;
            if (selectedKrs.getCourse() != null) {
                for (Course c : listMK) {
                    if (c.getCode().equals(selectedKrs.getCourse().getCode())) {
                        course = c;
                        jComboBoxCourse.setSelectedItem(c);
                        break;
                    }
                }
            }

            Lecturer lecturer = null;
            if (selectedKrs.getLecture() != null) {
                for (Lecturer l : listLecture) {
                    if (l.getNidn().equals(selectedKrs.getLecture().getNidn())) {
                        lecturer = l;
                        jComboBoxDosen.setSelectedItem(l);
                        break;
                    }
                }
            }

            comboModelSM.removeAllElements();
            comboModelSM.addElement(String.valueOf(selectedKrs.getSemester()));
            jComboBoxSM.setSelectedItem(String.valueOf(selectedKrs.getSemester()));
            jComboBoxSM.setEnabled(false);

            if (mahasiswa != null) {
                jLabelNIM.setText(mahasiswa.getNim() + " | Prodi: " + mahasiswa.getStudyProgram());
            }
            if (course != null) {
                jLabelKodeSKS.setText(course.getCode() + " | SKS: " + course.getSKS());
            }
            if (lecturer != null) {
                selecttedLecturer = lecturer;
            }
            jLabelHuruf.setText(selectedKrs.getGrade());
        } catch (Exception e) {
            System.err.println("Gagal menampilkan data KRS: " + e.getMessage());
            e.printStackTrace();
        }
    }

