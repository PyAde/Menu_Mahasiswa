    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        // TODO add your handling code here:
        try {
            if (selecttedStudent == null || selecttedCourse == null || selecttedLecturer == null) {
                javax.swing.JOptionPane.showMessageDialog(this,
                        "Pastikan Mahasiswa, Mata Kuliah, dan Dosen Pengampu sudah dipilih!",
                        "Peringatan",
                        javax.swing.JOptionPane.WARNING_MESSAGE);
                return;
            }

            int uas = Integer.parseInt(jTextFieldNilaiUAS.getText());
            int uts = Integer.parseInt(jTextFieldNilaiUTS.getText());
            int sikap = Integer.parseInt(jTextFieldNilaiSikap.getText());

            double rataRata = (uas + uts + sikap) / 3.0;
            this.krs = new KRS(selecttedCourse, rataRata);
            this.krs.setLecture(selecttedLecturer);

            int semester = Integer.parseInt(jComboBoxSM.getSelectedItem().toString());
            this.krs.setSemester(semester);

            this.krs.setStudent(selecttedStudent);

            selecttedStudent.addKRS(krs);
            jLabelHuruf.setText(this.krs.getGrade());
            
            Object[] rowData = {
                selecttedStudent.getNim(),
                selecttedStudent.getName(),
                krs.getCourse().getCourseName(),
                krs.getCourse().getSKS(),
                krs.getGrade(),
                krs.getLecture().getName()
            };

            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTableNilai.getModel();
            model.addRow(rowData);

            int hasil = krsController.create(this.krs);

            if (hasil == 1) {
                javax.swing.JOptionPane.showMessageDialog(this, "Data KRS berhasil disimpan ke Database!");
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Gagal menyimpan ke Database.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Terjadi kesalahan sistem: " + e.getMessage(),
                    "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonAddActionPerformed

