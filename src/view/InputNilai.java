    private void jComboBoxCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxCourseActionPerformed
        // TODO add your handling code here:
        this.selecttedCourse = (Course) jComboBoxCourse.getSelectedItem();

        if (selecttedCourse != null) {

            jLabelKodeSKS.setText(selecttedCourse.getCode() + " | SKS: " + selecttedCourse.getSKS());
            comboModelLecturer.removeAllElements();
            

            String namaMataKuliah = selecttedCourse.getCourseName().toLowerCase();
            for (Lecturer dosen : listLecture) {
                String keahlianDosen = dosen.getExpertise().toLowerCase();
                if (keahlianDosen.contains(namaMataKuliah) || namaMataKuliah.contains(keahlianDosen)) {
                    comboModelLecturer.addElement(dosen);
                }
            }

        }
    }//GEN-LAST:event_jComboBoxCourseActionPerformed

