    private void jButtonNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonNextActionPerformed
        // TODO add your handling code here:
        if (listKrs == null || listKrs.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Belum terdapat data KRS");
            return;
        }
        if (currentIndex < listKrs.size() - 1) {
            currentIndex++;
            tampilkanData(currentIndex);
            jTableNilai.setRowSelectionInterval(currentIndex, currentIndex);
            jTableNilai.scrollRectToVisible(jTableNilai.getCellRect(currentIndex, 0, true));
        }
    }//GEN-LAST:event_jButtonNextActionPerformed

    private void jButtonPrevActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPrevActionPerformed
        // TODO add your handling code here:
        if (listKrs == null || listKrs.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Belum terdapat data KRS");
            return;
        }
        if (currentIndex > 0) {
            currentIndex--;
            tampilkanData(currentIndex);
            jTableNilai.setRowSelectionInterval(currentIndex, currentIndex);
            jTableNilai.changeSelection(currentIndex, 0, false, false);
            jTableNilai.scrollRectToVisible(jTableNilai.getCellRect(currentIndex, 0, true));
            jTableNilai.requestFocusInWindow();
        }
    }//GEN-LAST:event_jButtonPrevActionPerformed

    private void jButtonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchActionPerformed
        // TODO add your handling code here:
        String keyword = jTextField1.getText().trim();
        List<KRS> dataHasil;
        
        // Logika if-else persis seperti di LecturerForm
        if (keyword.isEmpty()) {
            dataHasil = krsController.getAllKRS();
        } else {
            dataHasil = krsController.searchKRS(keyword);
        }

        refreshKrsTable(dataHasil);

        if (dataHasil.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Data KRS tidak ditemukan!",
                    "Pencarian",
                    javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButtonSearchActionPerformed
