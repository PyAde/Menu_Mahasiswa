    private void jButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanActionPerformed
        try {
            Course course = buildCourseFromForm();

            if (course == null) {
                javax.swing.JOptionPane.showMessageDialog(this,
                        "Semua data kolom mata kuliah harus terisi!",
                        "Peringatan",
                        javax.swing.JOptionPane.WARNING_MESSAGE);
                return;
            }

            int hasil;

            if (selectedCourseCode.isEmpty()) {
                hasil = controller.create(course);
            }else{
                hasil = controller.update(course, selectedCourseCode);
            }

            if (hasil == 1) {
                javax.swing.JOptionPane.showMessageDialog(this, "Data Course berhasil disimpan ke database");
                loadCourseData(); 
                clearForm();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this,
                        "Gagal menyimpan data.",
                        "Error",
                        javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Terjadi kesalahan sistem: " + e.getMessage(),
                    "Error Fatal",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButtonSimpanActionPerformed

    private void jButtonPrevActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPrevActionPerformed
        // TODO add your handling code here:
        if (listCourse == null || listCourse.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Belum ada data mata kuliah pada tabel");
            return;
        }
        if (selectedCourseIndex > 0) {
            showCourseAt(selectedCourseIndex - 1);
        }
    }//GEN-LAST:event_jButtonPrevActionPerformed

    private void jButtonNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonNextActionPerformed
        // TODO add your handling code here:
        if (listCourse == null || listCourse.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Belum ada data mata kuliah pada tabel");
            return;
        }
        if (selectedCourseIndex < listCourse.size() - 1) {
            showCourseAt(selectedCourseIndex + 1);
        }
    }//GEN-LAST:event_jButtonNextActionPerformed

    private void jToggleButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButtonDeleteActionPerformed
        // TODO add your handling code here:


        if (selectedCourseCode == null || selectedCourseCode.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                    "Pilih data tabel mata kuliah terlebih dahulu", 
                    "Peringatan", 
                    javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        // 2. Munculkan peringatan konfirmasi penghapusan
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this,
                "Apakah anda yakin untuk menghapus data mata kuliah ini? ",
                "Delete Confirmation",
                javax.swing.JOptionPane.YES_NO_OPTION
        );


        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            try {
                controller.deleteCourse(selectedCourseCode);
              
                javax.swing.JOptionPane.showMessageDialog(this, "Course deleted successfully!");
                this.clearForm(); 

                loadCourseData();

            } catch (Exception ex) {
                System.err.println("Error saat menghapus data course: " + ex.getMessage());
                javax.swing.JOptionPane.showMessageDialog(this,
                        "Deleted unsuccessfully! Terjadi kesalahan pada sistem.",
                        "Error",
                        javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jToggleButtonDeleteActionPerformed

