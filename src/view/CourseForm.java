    private void loadCourseData() {
        try {
            listCourse = controller.getAllCourses();
            updateTable(listCourse);

            if (listCourse != null && !listCourse.isEmpty()) {
                showCourseAt(0);
            } else {
                clearForm();
            }
        } catch (Exception e) {
            System.err.println("Gagal memuat data dari database: " + e.getMessage());
            e.printStackTrace();
        }
    }

    private void updateTable(List<Course> courses) {
        String[][] data = new String[courses.size()][4];

        for (int i = 0; i < courses.size(); i++) {
            Course course = courses.get(i);
            data[i][0] = course.getCode();
            data[i][1] = course.getCourseName();
            data[i][2] = String.valueOf(course.getSKS());
            data[i][3] = String.valueOf(course.getSemester());
        }

        String[] columnName = {"Code", "Nama Mata Kuliah", "SKS", "Semester"};
        jTableCourse.setModel(new DefaultTableModel(data, columnName));

        javax.swing.table.DefaultTableCellRenderer centerRenderer = new javax.swing.table.DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(javax.swing.JLabel.CENTER);
        jTableCourse.getColumnModel().getColumn(2).setCellRenderer(centerRenderer);
        jTableCourse.getColumnModel().getColumn(3).setCellRenderer(centerRenderer);
    }

    private void showCourseAt(int index) {
        if (listCourse == null || index < 0 || index >= listCourse.size()) {
            return;
        }

        Course course = listCourse.get(index);
        selectedCourseIndex = index;
        selectedCourseCode = course.getCode();

        jTextFieldCode.setText(course.getCode());
        jTextFieldNama.setText(course.getCourseName());
        jTextFieldSKS.setText(String.valueOf(course.getSKS()));
        jTextFieldSemester.setText(String.valueOf(course.getSemester()));
        jTextFieldCode.setEditable(false);

        if (jTableCourse.getRowCount() > index) {
            jTableCourse.setRowSelectionInterval(index, index);
            jTableCourse.scrollRectToVisible(jTableCourse.getCellRect(index, 0, true));
        }
    }

    private void clearForm() {
        jTextFieldNama.setText("");
        jTextFieldCode.setText("");
        jTextFieldSemester.setText("");
        jTextFieldSKS.setText("");
        selectedCourseIndex = -1;
        selectedCourseCode = "";
        jTextFieldCode.setEditable(true);
        
        jTextFieldCode.requestFocus();
    }

    private boolean isFormIncomplete(String courseName, String sksText, String code, String semesterText) {
        return courseName.isEmpty() || courseName.equals("Nama")
                || sksText.isEmpty() || sksText.equals("Sks")
                || code.isEmpty() || code.equals("Code")
                || semesterText.isEmpty() || semesterText.equals("Semester");
    }

    private Course buildCourseFromForm() {
        String courseName = jTextFieldNama.getText().trim();
        String sksText = jTextFieldSKS.getText().trim();
        String code = jTextFieldCode.getText().trim();
        String semesterText = jTextFieldSemester.getText().trim();

        if (isFormIncomplete(courseName, sksText, code, semesterText)) {
            return null;
        }

        return new Course(code, courseName, Integer.parseInt(sksText), Integer.parseInt(semesterText));
    }
    


